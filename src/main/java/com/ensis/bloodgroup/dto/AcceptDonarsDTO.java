package com.ensis.bloodgroup.dto;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author Ensis
 *
 */
@Entity
@Table(name = "accepteddoners")
public class AcceptDonarsDTO {
	
	

	@Id
	private int acceptid;
	private int requeststatusid;
	private int donarid;
	private Date createddate;
	/**
	 * @return the acceptid
	 */
	public int getAcceptid() {
		return acceptid;
	}
	/**
	 * @param acceptid the acceptid to set
	 */
	public void setAcceptid(int acceptid) {
		this.acceptid = acceptid;
	}
	/**
	 * @return the requeststatusid
	 */
	public int getRequeststatusid() {
		return requeststatusid;
	}
	/**
	 * @param requeststatusid the requeststatusid to set
	 */
	public void setRequeststatusid(int requeststatusid) {
		this.requeststatusid = requeststatusid;
	}
	/**
	 * @return the donarid
	 */
	public int getDonarid() {
		return donarid;
	}
	/**
	 * @param donarid the donarid to set
	 */
	public void setDonarid(int donarid) {
		this.donarid = donarid;
	}
	/**
	 * @return the createddate
	 */
	public Date getCreateddate() {
		return createddate;
	}
	/**
	 * @param createddate the createddate to set
	 */
	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}
	
	
	
}
