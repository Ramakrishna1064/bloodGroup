public class DonarListOfRequestsModel {
	
	private int requestid;
	private String organizationname;
	private int donarid;
	private String bloodgroup;
	private int bloodquantity;
	private String requesteddate;
	
	
	
	/**
	 * @return the requestid
	 */
	public int getRequestid() {
		return requestid;
	}
	/**
	 * @param requestid the requestid to set
	 */
	public void setRequestid(int requestid) {
		this.requestid = requestid;
	}
	/**
	 * @return the organizationname
	 */
	public String getOrganizationname() {
		return organizationname;
	}
	/**
	 * @param organizationname the organizationname to set
	 */
	public void setOrganizationname(String organizationname) {
		this.organizationname = organizationname;
	}
	/**
	 * @return the donarid
	 */
	public int getDonarid() {
		return donarid;
	}
	/**
	 * @param donarid the donarid to set
	 */
	public void setDonarid(int donarid) {
		this.donarid = donarid;
	}
	/**
	 * @return the bloodgroup
	 */
	public String getBloodgroup() {
		return bloodgroup;
	}
	/**
	 * @param bloodgroup the bloodgroup to set
	 */
	public void setBloodgroup(String bloodgroup) {
		this.bloodgroup = bloodgroup;
	}
	/**
	 * @return the bloodquantity
	 */
	public int getBloodquantity() {
		return bloodquantity;
	}
	/**
	 * @param bloodquantity the bloodquantity to set
	 */
	public void setBloodquantity(int bloodquantity) {
		this.bloodquantity = bloodquantity;
	}
	/**
	 * @return the requesteddate
	 */
	public String getRequesteddate() {
		return requesteddate;
	}
	/**
	 * @param requesteddate the requesteddate to set
	 */
	public void setRequesteddate(String requesteddate) {
		this.requesteddate = requesteddate;
	}
	
	
	
	
	
	

}
